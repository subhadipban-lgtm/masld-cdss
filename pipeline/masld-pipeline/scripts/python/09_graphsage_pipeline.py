@torch.no_grad()
def predict_and_rank_links_fast(model, train_data, original_G_nx, top_k=20):
    # This function is complex and its logic remains largely unchanged, focusing on scoring plausible candidates
    model.eval()
    z = model.encode(train_data.x.to(device), train_data.edge_index.to(device))
    z = z.cpu()
    node_map_int = {label: i for i, label in enumerate(sorted(original_G_nx.nodes()))}
    all_pos_edges_set = {tuple(sorted((node_map_int[u], node_map_int[v]))) for u, v in original_G_nx.edges()}
    node_types = nx.get_node_attributes(original_G_nx, "type")
    plausible_types = {tuple(sorted((node_types.get(u, 'unknown'), node_types.get(v, 'unknown')))) for u, v in original_G_nx.edges()}
    int_to_label = {i: label for i, label in enumerate(train_data.node_labels)}
    int_to_type = {i: node_types.get(label, 'unknown') for i, label in int_to_label.items()}
    nodes_by_type = defaultdict(list)
    for node_idx, n_type in int_to_type.items(): nodes_by_type[n_type].append(node_idx)
    candidates = [tuple(sorted((u, v))) for type_a, type_b in plausible_types for u in nodes_by_type[type_a] for v in nodes_by_type[type_b] if u < v]
    candidates = list(set(candidates) - all_pos_edges_set)
    results, batch_size = [], 16384
    for i in range(0, len(candidates), batch_size):
        batch = candidates[i:i+batch_size]
        if not batch: continue
        src, dst = zip(*batch)
        src, dst = torch.tensor(src, device=z.device), torch.tensor(dst, device=z.device)
        scores = (z[src] * z[dst]).sum(dim=-1)
        probs = torch.sigmoid(scores).cpu().tolist()
        results.extend(zip(batch, probs))
    results.sort(key=lambda x: x[1], reverse=True)
    rows = []
    labels = train_data.node_labels
    for (a, b), s in results[:top_k]:
        la, lb = labels[a], labels[b]
        rows.append({"Source": la, "Source_Type": node_types.get(la, "N/A"), "Target": lb, "Target_Type": node_types.get(lb, "N/A"), "Score": s})
    return pd.DataFrame(rows)

