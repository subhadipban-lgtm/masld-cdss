def calculate_wgcna_baseline_ari(gexf_path, wgcna_csv_path):
    """
    Calculates ARI comparing GNN fibrosis module vs. WGCNA fibrosis module.
    """
    # 1. Load the GNN-enhanced Knowledge Graph
    print(f"Loading GNN Graph: {gexf_path}...")
    try:
        G = nx.read_gexf(gexf_path)
    except Exception as e:
        print(f"Error loading GEXF: {e}")
        return

    # 2. Load the WGCNA baseline genes
    print(f"Loading WGCNA baseline: {wgcna_csv_path}...")
    wgcna_df = pd.read_csv(wgcna_csv_path)
    # Ensure we have a set of gene names for fast lookup
    # Assuming column name is 'genes' based on file preview
    wgcna_genes = set(wgcna_df['genes'].astype(str).unique())

    # 3. Align genes between the two datasets
    # Filter for nodes of type 'gene' in the graph
    gene_nodes = [n for n, attr in G.nodes(data=True) if attr.get('type') == 'gene']

    if not gene_nodes:
        print("Warning: No nodes of type 'gene' found in the GEXF file.")
        return

    gnn_labels = []
    wgcna_labels = []

    for gene in gene_nodes:
        # GNN Label: Based on the attribute in the GEXF
        # (Assuming 'in_fibrosis_module' is the boolean attribute used in your GNN)
        is_in_gnn = G.nodes[gene].get('in_fibrosis_module', False)
        # Convert to int (1 if True, 0 if False)
        # Note: GEXF booleans often load as strings 'true'/'false' or '0'/'1'
        if isinstance(is_in_gnn, str):
            is_in_gnn = is_in_gnn.lower() == 'true'
        gnn_labels.append(int(is_in_gnn))

        # WGCNA Label: 1 if the gene is in the CSV, 0 otherwise
        is_in_wgcna = gene in wgcna_genes
        wgcna_labels.append(int(is_in_wgcna))

    # 4. Calculate ARI
    ari_value = adjusted_rand_score(gnn_labels, wgcna_labels)

    # Calculate Overlap Statistics
    intersection = sum(1 for g, w in zip(gnn_labels, wgcna_labels) if g == 1 and w == 1)
    gnn_total = sum(gnn_labels)
    wgcna_total = sum(wgcna_labels)

    print("\n" + "="*40)
    print("Baseline Comparison: GNN vs. WGCNA")
    print("="*40)
    print(f"Total Genes Evaluated: {len(gene_nodes)}")
    print(f"GNN Fibrosis Module Size: {gnn_total}")
    print(f"WGCNA Fibrosis Module Size: {wgcna_total}")
    print(f"Overlapping Genes: {intersection}")
    print(f"\nADJUSTED RAND INDEX (ARI): {ari_value:.4f}")
    print("="*40)

    # 5. Interpretation
    if ari_value > 0.7:
        print("Interpretation: Excellent agreement. The GNN and WGCNA identify nearly identical fibrosis programs.")
    elif ari_value > 0.4:
        print("Interpretation: Moderate agreement. The GNN refines the WGCNA core with additional topological context.")
    else:
        print("Interpretation: Low agreement. The GNN identifies a distinct regulatory circuit not captured by linear co-expression.")

    return ari_value

