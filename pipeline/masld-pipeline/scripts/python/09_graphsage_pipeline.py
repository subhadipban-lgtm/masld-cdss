    candidates = [tuple(sorted((u, v))) for type_a, type_b in plausible_types for u in nodes_by_type[type_a] for v in nodes_by_type[type_b] if u < v]
    candidates = list(set(candidates) - all_pos_edges_set)
    results, batch_size = [], 16384
    for i in range(0, len(candidates), batch_size):
        batch = candidates[i:i+batch_size]
        if not batch: continue
        src, dst = zip(*batch)
        src, dst = torch.tensor(src, device=z.device), torch.tensor(dst, device=z.device)
        scores = (z[src] * z[dst]).sum(dim=-1)
        probs = torch.sigmoid(scores).cpu().tolist()
        results.extend(zip(batch, probs))
    results.sort(key=lambda x: x[1], reverse=True)
    rows = []
    labels = train_data.node_labels
    for (a, b), s in results[:top_k]:
        la, lb = labels[a], labels[b]
        rows.append({"Source": la, "Source_Type": node_types.get(la, "N/A"), "Target": lb, "Target_Type": node_types.get(lb, "N/A"), "Score": s})
    return pd.DataFrame(rows)

