def run_pipeline_for_graph(gexf_file, title, all_node_types, all_personalization_statuses, holdout_type=None, balance_dataset=False, use_2hop_sampling=False):
    full_title = title
    title_tags = []
    if holdout_type: title_tags.append(f"Holdout:{'_'.join(sorted(holdout_type))}")
    if balance_dataset: title_tags.append("Balanced")
    if use_2hop_sampling: title_tags.append("2-Hop")
    if title_tags: full_title += f" ({', '.join(title_tags)})";

    print(f"\n{'='*80}\nRUNNING PIPELINE FOR: {full_title}\n{'='*80}")
    G_nx = nx.read_gexf(gexf_file)
    data = preprocess_graph_for_pyg(G_nx, all_node_types, all_personalization_statuses)

    train_data, val_data, test_data = create_hard_negative_splits(data, G_nx, val_size=0.05, test_size=0.1, holdout_type=holdout_type, balance_dataset=balance_dataset, use_2hop_sampling=use_2hop_sampling)
    for d in (train_data, val_data, test_data): d.to(device)

    model = GraphSAGE_LinkPredictor(data.x.size(1), 64, 32).to(device)
    optimizer = torch.optim.Adam(model.parameters(), lr=0.005, weight_decay=5e-4)
    pos_weight = compute_pos_weight(train_data.edge_label).to(device)
    print(f"pos_weight = {pos_weight.item():.3f}")

    print("\nStarting training with early stopping...")
    best_metric, best_epoch, patience, counter = 0, 0, 20, 0
    best_state, history = None, defaultdict(list)
    for epoch in range(1, 301):
        loss = train(model, train_data, optimizer, pos_weight)
        acc, ap, roc_auc, _, _ = evaluate(model, val_data)
        history["epoch"].append(epoch); history["loss"].append(loss); history["val_ap"].append(ap); history["val_auc"].append(roc_auc)
        if roc_auc > best_metric: best_metric, best_epoch, counter, best_state = roc_auc, epoch, 0, model.state_dict()
