def create_hard_negative_splits(data, G_nx, val_size=0.05, test_size=0.1, holdout_type=None, balance_dataset=False, use_2hop_sampling=False):
    """
    Performs advanced negative sampling and splitting with options for balancing and 2-hop negatives.
    """
    print("Starting hard negative sampling...")
    strategy_log = []
    if balance_dataset: strategy_log.append("1:1 Balanced")
    if use_2hop_sampling: strategy_log.append("2-Hop Sampling")
    if strategy_log: print(f"  - Strategies: {', '.join(strategy_log)}")

    if holdout_type:
        holdout_type = tuple(sorted(holdout_type))
        print(f"  - HOLDOUT MODE: Testing on edge type {holdout_type}")

    node_types = nx.get_node_attributes(G_nx, "type")
    int_to_label = {i: label for i, label in enumerate(data.node_labels)}
    int_to_type = {i: node_types.get(label, 'unknown') for i, label in int_to_label.items()}
    nodes_by_type = defaultdict(list)
    for node_idx, n_type in int_to_type.items(): nodes_by_type[n_type].append(node_idx)

    pos_edge_set = {tuple(sorted((u, v))) for u, v in data.edge_index.t().tolist()}
    print(f"  - Found {len(pos_edge_set)} unique positive edges.")

    edge_types_to_sample = defaultdict(int)
    pos_edges_by_type = defaultdict(list)
    for u, v in pos_edge_set:
        edge_type = tuple(sorted((int_to_type[u], int_to_type[v])))
        edge_types_to_sample[edge_type] += 1
        pos_edges_by_type[edge_type].append((u, v))
    print(f"  - Found plausible edge types: {list(edge_types_to_sample.keys())}")

    # --- Negative Sampling ---
    neg_edge_set = set()
    target_neg_count = len(pos_edge_set)

    # Strategy 2: 2-Hop Negative Sampling
    if use_2hop_sampling:
        adj = defaultdict(set)
        for u, v in pos_edge_set:
            adj[u].add(v)
            adj[v].add(u)

        for u, v in list(pos_edge_set):
            if len(neg_edge_set) >= target_neg_count: break
            # Find 2-hop neighbors of u
            two_hop_neighbors = set()
            for neighbor in adj[u]:
                for two_hop in adj[neighbor]:
                    two_hop_neighbors.add(two_hop)

            # Filter for valid candidates
            candidates = [w for w in two_hop_neighbors if w != u and w not in adj[u] and int_to_type[w] == int_to_type[v]]
            if candidates:
                w = random.choice(candidates)
                pair = tuple(sorted((u, w)))
                if pair not in pos_edge_set:
                    neg_edge_set.add(pair)
        print(f"  - Generated {len(neg_edge_set)} negatives via 2-hop sampling.")

    # Fallback/Default: Random Plausible Sampling
    if len(neg_edge_set) < target_neg_count:
        print("  - Using random sampling for remaining negatives...")
        remaining_needed = target_neg_count - len(neg_edge_set)
        # This part is complex to get exactly right, so we simplify by just using the main loop
        for edge_type, count in edge_types_to_sample.items():
            type_a, type_b = edge_type
            nodes_a, nodes_b = nodes_by_type[type_a], nodes_by_type[type_b]
            if not nodes_a or not nodes_b: continue
            for _ in range(count * 10):
                if len(neg_edge_set) >= target_neg_count: break
                u, v = random.choice(nodes_a), random.choice(nodes_b)
                if u == v: continue
                pair = tuple(sorted((u, v)))
                if pair not in pos_edge_set and pair not in neg_edge_set:
                    neg_edge_set.add(pair)

    print(f"  - Generated a total of {len(neg_edge_set)} hard negative edges.")

    pos_edges_list = list(pos_edge_set)
    neg_edges_list = list(neg_edge_set)

    # Strategy 1: Data Balancing
    if balance_dataset:
        print("  - Balancing dataset to 1:1 ratio...")
        min_count = min(len(pos_edges_list), len(neg_edges_list))
        print(f"  - Downsampling to {min_count} positive and {min_count} negative edges.")
        pos_edges_list = random.sample(pos_edges_list, min_count)
        neg_edges_list = random.sample(neg_edges_list, min_count)

    # Rebuild by-type dicts from potentially downsampled lists
    pos_edges_by_type = defaultdict(list)
    for u,v in pos_edges_list: pos_edges_by_type[tuple(sorted((int_to_type[u], int_to_type[v])))].append((u,v))
    neg_edges_by_type = defaultdict(list)
    for u,v in neg_edges_list: neg_edges_by_type[tuple(sorted((int_to_type[u], int_to_type[v])))].append((u,v))

    # --- Splitting Logic (handles holdout) ---
    train_val_pos, test_pos, train_val_neg, test_neg = [], [], [], []
    for edge_type, edges in pos_edges_by_type.items():
        if holdout_type and edge_type == holdout_type: test_pos.extend(edges)
        else: train_val_pos.extend(edges)
    for edge_type, edges in neg_edges_by_type.items():
        if holdout_type and edge_type == holdout_type: test_neg.extend(edges)
        else: train_val_neg.extend(edges)

    random.shuffle(train_val_pos); random.shuffle(train_val_neg)

    # ... (rest of the splitting and data object creation is the same)
    val_count_pos = int(len(train_val_pos) * val_size / (1 - test_size)) if (1 - test_size) > 0 else 0
    val_count_neg = int(len(train_val_neg) * val_size / (1 - test_size)) if (1 - test_size) > 0 else 0
    val_pos, train_pos = train_val_pos[:val_count_pos], train_val_pos[val_count_pos:]
    val_neg, train_neg = train_val_neg[:val_count_neg], train_val_neg[val_count_neg:]
    if not holdout_type:
        test_count_pos = int(len(train_pos) * test_size / (1 - val_size - test_size)) if (1 - val_size - test_size) > 0 else 0
        test_count_neg = int(len(train_neg) * test_size / (1 - val_size - test_size)) if (1 - val_size - test_size) > 0 else 0
        test_pos, train_pos = train_pos[:test_count_pos], train_pos[test_count_pos:]
        test_neg, train_neg = train_neg[:test_count_neg], train_neg[test_count_neg:]

    def create_data_split(pos_list, neg_list, train_edges_tensor):
        split_data = Data(x=data.x, edge_index=to_undirected(train_edges_tensor, num_nodes=data.num_nodes), node_labels=data.node_labels, num_nodes=data.num_nodes)
        pos_tensor = torch.tensor(pos_list, dtype=torch.long).t().contiguous() if pos_list else torch.empty((2, 0), dtype=torch.long)
        neg_tensor = torch.tensor(neg_list, dtype=torch.long).t().contiguous() if neg_list else torch.empty((2, 0), dtype=torch.long)
        split_data.edge_label_index = torch.cat([pos_tensor, neg_tensor], dim=1)
        split_data.edge_label = torch.cat([torch.ones(pos_tensor.size(1)), torch.zeros(neg_tensor.size(1))]).float()
        return split_data

    train_pos_tensor = torch.tensor(train_pos, dtype=torch.long).t().contiguous() if train_pos else torch.empty((2, 0), dtype=torch.long)

    print("  - Creating final data splits...")
    train_data = create_data_split(train_pos, train_neg, train_pos_tensor)
    val_data = create_data_split(val_pos, val_neg, train_pos_tensor)
    test_data = create_data_split(test_pos, test_neg, train_pos_tensor)
    print("Hard negative splitting complete.")
    return train_data, val_data, test_data


def compute_pos_weight(edge_label):
    pos = (edge_label == 1).sum()
    neg = (edge_label == 0).sum()
    if pos == 0 or neg == 0: return torch.tensor(1.0)
    return neg / pos

def train(model, data, optimizer, pos_weight=None):
    model.train()
    optimizer.zero_grad()
    z = model.encode(data.x, data.edge_index)
    out = model.decode(z, data.edge_label_index)
    target = data.edge_label.float().to(device)
    criterion = nn.BCEWithLogitsLoss(pos_weight=pos_weight)
    loss = criterion(out, target)
    loss.backward()
    optimizer.step()
    return loss.item()

@torch.no_grad()
def evaluate(model, data):
    model.eval()
    z = model.encode(data.x, data.edge_index)
    if data.edge_label_index.size(1) == 0: return 0.5, 0.5, 0.5, np.array([]), np.array([])
    out = model.decode(z, data.edge_label_index)
    scores = torch.sigmoid(out)
    scores_np, target_np = scores.cpu().numpy(), data.edge_label.cpu().numpy()
    if len(np.unique(target_np)) < 2: return 0.5, 0.5, 0.5, scores_np, target_np
    ap, roc_auc = average_precision_score(target_np, scores_np), roc_auc_score(target_np, scores_np)
    acc = ((scores_np > 0.5).astype(int) == target_np).mean()
    return acc, ap, roc_auc, scores_np, target_np

