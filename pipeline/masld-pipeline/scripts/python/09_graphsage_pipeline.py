class GraphSAGE_LinkPredictor(nn.Module):
    def __init__(self, in_channels, hidden_channels, out_channels):
        super().__init__()
        self.conv1 = SAGEConv(in_channels, hidden_channels)
        self.conv2 = SAGEConv(hidden_channels, out_channels)

    def encode(self, x, edge_index):
        x = self.conv1(x, edge_index)
        x = F.relu(x)
        x = F.dropout(x, p=0.3, training=self.training)
        x = self.conv2(x, edge_index)
        return x

    def decode(self, z, edge_label_index):
        src, dst = edge_label_index
        return (z[src] * z[dst]).sum(dim=-1)

def compute_pos_weight(edge_label):
    pos = (edge_label == 1).sum()
    neg = (edge_label == 0).sum()
    return neg / pos if pos > 0 and neg > 0 else torch.tensor(1.0)

def train(model, data, optimizer, pos_weight=None):
    model.train()
    optimizer.zero_grad()
    z = model.encode(data.x, data.edge_index)
    out = model.decode(z, data.edge_label_index)
    target = data.edge_label.float().to(device)
    loss = nn.BCEWithLogitsLoss(pos_weight=pos_weight)(out, target)
    loss.backward()
    optimizer.step()
    return loss.item()

@torch.no_grad()
def evaluate(model, data, test_edge_label_index=None, test_edge_label=None):
    model.eval()
    z = model.encode(data.x, data.edge_index)
    eval_edge_label_index = test_edge_label_index if test_edge_label_index is not None else data.edge_label_index
    eval_edge_label = test_edge_label if test_edge_label is not None else data.edge_label

    if eval_edge_label_index.size(1) == 0:
        return 0.5, 0.5, 0.5, np.array([]), np.array([])

    out = model.decode(z, eval_edge_label_index)
    scores = torch.sigmoid(out)
    scores_np, target_np = scores.cpu().numpy(), eval_edge_label.cpu().numpy()

    if len(np.unique(target_np)) < 2:
        return 0.5, 0.5, 0.5, scores_np, target_np

    ap, roc_auc = average_precision_score(target_np, scores_np), roc_auc_score(target_np, scores_np)
    acc = ((scores_np > 0.5).astype(int) == target_np).mean()

    return acc, ap, roc_auc, scores_np, target_np

