def leave_one_class_out_cv(base_pyg_data, real_edge_label_index, real_edge_label, real_drug_classes, num_epochs=200, patience=20):
    # Removed global declarations for functions/classes now defined within this cell
    global G_test_nx, SEED, class_mapping, device

    unique_classes = np.unique(real_drug_classes)
    fold_results = [] # Store results for each fold

    for i, holdout_class in enumerate(unique_classes):
        print(f"\n--- LOCO CV Fold {i+1}/{len(unique_classes)}: Holding out Class '{holdout_class}' ---")

        # 1. Split real_edge_label_index and real_edge_label into train/test for this fold
        holdout_indices = np.where(real_drug_classes == holdout_class)[0]
        train_indices = np.where(real_drug_classes != holdout_class)[0]

        if len(holdout_indices) == 0:
            print(f"  WARNING: No edges found for class '{holdout_class}'. Skipping this fold.")
            continue
        if len(train_indices) == 0:
            print(f"  WARNING: No training edges available after holding out class '{holdout_class}'. Skipping this fold.")
            continue

        cv_train_edge_label_index = real_edge_label_index[:, train_indices].to(device)
        cv_train_edge_label = real_edge_label[train_indices].to(device)
        cv_test_edge_label_index = real_edge_label_index[:, holdout_indices].to(device)
        cv_test_edge_label = real_edge_label[holdout_indices].to(device)

        # 2. Construct the message-passing graph for this fold
        #    Remove edges from G_test_nx where the source drug belongs to the 'holdout_class'.

        G_mp_fold_nx = G_test_nx.copy()

        edges_to_remove = []
        for u_label, v_label in G_mp_fold_nx.edges():
            if G_mp_fold_nx.nodes[u_label].get('type') == 'drug':
                node_drug_class = "Other_or_Gene"
                for drug_name, pharm_class in class_mapping.items():
                    if drug_name.lower() in u_label.lower():
                        node_drug_class = pharm_class
                        break
                if node_drug_class == holdout_class:
                    edges_to_remove.append((u_label, v_label))

        G_mp_fold_nx.remove_edges_from(edges_to_remove)

        # Convert the filtered NetworkX graph to PyG Data object's edge_index
        node_label_to_idx = {label: idx for idx, label in enumerate(base_pyg_data.node_labels)}
        mp_edges = []
        for u_label, v_label in G_mp_fold_nx.edges():
            if u_label in node_label_to_idx and v_label in node_label_to_idx:
                mp_edges.append((node_label_to_idx[u_label], node_label_to_idx[v_label]))

        mp_edge_index = torch.tensor(mp_edges, dtype=torch.long).t().contiguous() if mp_edges else torch.empty((2, 0), dtype=torch.long)
        mp_edge_index = to_undirected(mp_edge_index, num_nodes=base_pyg_data.num_nodes).to(device)

        # Create the data object for training this fold
        fold_train_data = Data(x=base_pyg_data.x.to(device), edge_index=mp_edge_index)
        fold_train_data.edge_label_index = cv_train_edge_label_index
        fold_train_data.edge_label = cv_train_edge_label

        # 3. Initialize and train a new model for each fold
        model = GraphSAGE_LinkPredictor(base_pyg_data.x.size(1), 64, 32).to(device)
        optimizer = torch.optim.Adam(model.parameters(), lr=0.005, weight_decay=5e-4)
        pos_weight = compute_pos_weight(fold_train_data.edge_label).to(device)

        print(f"  Training model for fold with {fold_train_data.edge_label_index.size(1)} train links...")
        best_val_auc, best_epoch, counter = 0, 0, 0

        for epoch in range(1, num_epochs + 1):
            loss = train(model, fold_train_data, optimizer, pos_weight)

            # Evaluate validation metric on train_split.edge_label_index (for monitoring, not for early stopping based on this)
            if epoch % 20 == 0:
                _, _, current_val_auc, _, _ = evaluate(model, fold_train_data, fold_train_data.edge_label_index, fold_train_data.edge_label)
                print(f"    Epoch {epoch:03d} | Train Loss: {loss:.4f} | Internal Train AUC: {current_val_auc:.3f}")

            # Early stopping based on internal validation or just run for fixed epochs
            # For simplicity, we can just run for full epochs or a simpler early stop on training AUC.
            # A robust LOCO CV often includes an internal validation set within the training split for early stopping.
            # Here, we will just use the last model of the fixed epochs for test evaluation.


        # 4. Evaluate the trained model on the fold's test set
        acc, ap, roc_auc, _, _ = evaluate(model, fold_train_data, cv_test_edge_label_index, cv_test_edge_label)
        fold_results.append({
            'holdout_class': holdout_class,
            'accuracy': acc,
            'ap': ap,
            'roc_auc': roc_auc
        })
        print(f"  Fold '{holdout_class}' Test Acc: {acc:.3f}, AP: {ap:.3f}, AUC: {roc_auc:.3f}")
        print(f"  Model training for class '{holdout_class}' completed.")

    # Aggregate and print overall LOCO CV results
    if fold_results:
        results_df = pd.DataFrame(fold_results)
        print("\n=== LOCO CV Summary ===")
        print(results_df.round(3))
        print(f"\nAverage LOCO CV Accuracy: {results_df['accuracy'].mean():.3f}")
        print(f"Average LOCO CV AP: {results_df['ap'].mean():.3f}")
        print(f"Average LOCO CV AUC: {results_df['roc_auc'].mean():.3f}")
    else:
        print("\nNo LOCO CV results to display.")

    return fold_results


