def calculate_module_robustness(G, attribute_name='in_fibrosis_module', algorithm='louvain'):
    """
    Compares detected communities against the manuscript's predefined modules
    using different community detection algorithms.
    """
    # 1. Get the 'Ground Truth' labels from your GEXF attributes
    nodes = list(G.nodes())
    # Ensure all nodes have the attribute, defaulting to False if not present
    # Convert boolean to int for ARI calculation if necessary (0 for False, 1 for True)
    ground_truth = [int(G.nodes[n].get(attribute_name, False)) for n in nodes]

    # 2. Detect communities using the specified algorithm
    detected_labels = []

    if algorithm == 'louvain':
        print(f"Detecting communities using Louvain algorithm...")
        # Ensure the graph is undirected for Louvain
        partition = community_louvain.best_partition(G.to_undirected())
        detected_labels = [partition[n] for n in nodes]

    elif algorithm == 'leiden':
        print(f"Detecting communities using Leiden algorithm...")
        # Convert NetworkX graph to iGraph for Leiden
        G_ig = ig.Graph.from_networkx(G)
        # Using ResolutionParameter method for modularity maximization
        # You might need to tune 'resolution_parameter' and 'weights'
        part = la.find_partition(G_ig, la.ModularityVertexPartition)
        # Map iGraph clusters back to original NetworkX nodes
        # Leiden returns a VertexClustering object, use .membership
        # Ensure node order matches between NetworkX and iGraph conversions
        nx_nodes_to_ig_idx = {name: index for index, name in enumerate(G.nodes())}
        detected_labels = [part.membership[nx_nodes_to_ig_idx[n]] for n in nodes]

    else:
        raise ValueError(f"Unsupported algorithm: {algorithm}. Choose 'louvain' or 'leiden'.")


    # 3. Calculate Adjusted Rand Index (ARI)
    # ARI = 1.0 (Perfect match), ARI = 0.0 (Random), ARI < 0.0 (Uncorrelated)
    # Check if there are at least two distinct values in both arrays
    if len(np.unique(ground_truth)) < 2 or len(np.unique(detected_labels)) < 2:
        print("Warning: Not enough unique labels in ground truth or detected communities to calculate ARI meaningfully. Returning 0.")
        score = 0.0
    else:
        score = adjusted_rand_score(ground_truth, detected_labels)

    print(f"Adjusted Rand Index for {algorithm.capitalize()}: {score:.4f}")
    if score > 0.7:
        print("  Strong Evidence: The GNN modules are highly robust and biologically consistent.")
    elif score > 0.4:
        print("  Moderate Evidence: The modules capture significant biological signal.")
    else:
        print("  Warning: Low overlap. Re-evaluate clustering parameters or try a different algorithm.")

    return score

