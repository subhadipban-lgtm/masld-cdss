def create_hard_negative_splits(data, G_nx_mapped, int_to_type, val_size=0.05, test_size=0.1, balance_dataset=False):
    if balance_dataset: print("  - Strategy: 1:1 Balanced")
    nodes_by_type = defaultdict(list)
    for node_idx, n_type in int_to_type.items(): nodes_by_type[n_type].append(node_idx)
    pos_edge_set = {tuple(sorted((u, v))) for u, v in data.edge_index.t().tolist()}
    edge_types_to_sample = defaultdict(int)
    for u, v in pos_edge_set: edge_types_to_sample[tuple(sorted((int_to_type[u], int_to_type[v])))] += 1
    neg_edge_set = set()
    target_neg_count = len(pos_edge_set)
    for edge_type, count in edge_types_to_sample.items():
        type_a, type_b = edge_type
        nodes_a, nodes_b = nodes_by_type[type_a], nodes_by_type[type_b]
        if not nodes_a or not nodes_b: continue
        for _ in range(count * 10):
            if len(neg_edge_set) >= target_neg_count: break
            u, v = random.choice(nodes_a), random.choice(nodes_b)
            if u == v: continue
            pair = tuple(sorted((u, v)))
            if pair not in pos_edge_set and pair not in neg_edge_set: neg_edge_set.add(pair)
    pos_edges_list, neg_edges_list = list(pos_edge_set), list(neg_edge_set)
    if balance_dataset:
        min_count = min(len(pos_edges_list), len(neg_edges_list))
        pos_edges_list = random.sample(pos_edges_list, min_count)
        neg_edges_list = random.sample(neg_edges_list, min_count)
    random.shuffle(pos_edges_list); random.shuffle(neg_edges_list)
    val_count_pos = int(len(pos_edges_list) * val_size); test_count_pos = int(len(pos_edges_list) * test_size); train_count_pos = len(pos_edges_list) - val_count_pos - test_count_pos
    val_count_neg = int(len(neg_edges_list) * val_size); test_count_neg = int(len(neg_edges_list) * test_size); train_count_neg = len(neg_edges_list) - val_count_neg - test_count_neg
    train_pos, val_pos, test_pos = pos_edges_list[:train_count_pos], pos_edges_list[train_count_pos:train_count_pos+val_count_pos], pos_edges_list[train_count_pos+val_count_pos:]
    train_neg, val_neg, test_neg = neg_edges_list[:train_count_neg], neg_edges_list[train_count_neg:train_count_neg+val_count_neg], neg_edges_list[train_count_neg+val_count_neg:]
    def create_data_split(pos_list, neg_list, train_edges_tensor):
        split_data = Data(x=data.x, edge_index=to_undirected(train_edges_tensor, num_nodes=data.num_nodes), node_labels=data.node_labels, num_nodes=data.num_nodes)
        pos_tensor = torch.tensor(pos_list, dtype=torch.long).t().contiguous() if pos_list else torch.empty((2, 0), dtype=torch.long)
        neg_tensor = torch.tensor(neg_list, dtype=torch.long).t().contiguous() if neg_list else torch.empty((2, 0), dtype=torch.long)
        split_data.edge_label_index = torch.cat([pos_tensor, neg_tensor], dim=1)
        split_data.edge_label = torch.cat([torch.ones(pos_tensor.size(1)), torch.zeros(neg_tensor.size(1))]).float()
        return split_data
    train_pos_tensor = torch.tensor(train_pos, dtype=torch.long).t().contiguous() if train_pos else torch.empty((2, 0), dtype=torch.long)
    return create_data_split(train_pos, train_neg, train_pos_tensor), create_data_split(val_pos, val_neg, train_pos_tensor), create_data_split(test_pos, test_neg, train_pos_tensor)

def compute_pos_weight(edge_label):
    pos = (edge_label == 1).sum(); neg = (edge_label == 0).sum()
    return neg / pos if pos > 0 and neg > 0 else torch.tensor(1.0)

def train(model, data, optimizer, pos_weight=None):
    model.train(); optimizer.zero_grad()
    z = model.encode(data.x, data.edge_index)
    out = model.decode(z, data.edge_label_index)
    target = data.edge_label.float().to(device)
    loss = nn.BCEWithLogitsLoss(pos_weight=pos_weight)(out, target)
    loss.backward(); optimizer.step()
    return loss.item()

@torch.no_grad()
def evaluate(model, data, test_edge_label_index=None, test_edge_label=None):
    """
    MODIFIED: Now returns (acc, ap, roc_auc, scores_np, target_np)
    This provides the raw scores and labels needed for plotting curves.
    """
    model.eval()
    z = model.encode(data.x, data.edge_index)
    eval_edge_label_index = test_edge_label_index if test_edge_label_index is not None else data.edge_label_index
    eval_edge_label = test_edge_label if test_edge_label is not None else data.edge_label

    if eval_edge_label_index.size(1) == 0:
        # Return 5 values on failure
        return 0.5, 0.5, 0.5, np.array([]), np.array([])

    out = model.decode(z, eval_edge_label_index)
    scores = torch.sigmoid(out)
    scores_np, target_np = scores.cpu().numpy(), eval_edge_label.cpu().numpy()

    if len(np.unique(target_np)) < 2:
        # Return 5 values if only one class is present
        return 0.5, 0.5, 0.5, scores_np, target_np

    ap, roc_auc = average_precision_score(target_np, scores_np), roc_auc_score(target_np, scores_np)
    acc = ((scores_np > 0.5).astype(int) == target_np).mean()

    # Return scores and targets
    return acc, ap, roc_auc, scores_np, target_np

