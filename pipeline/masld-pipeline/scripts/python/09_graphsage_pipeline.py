def parse_targets_csv(file_path):
    true_targets_map = defaultdict(list)
    gene_regex = re.compile(r'\((.*?)\)')
    with open(file_path, 'r', encoding='utf-8') as f:
        reader = csv.reader(f)
        header = next(reader)
        drug_col_name, target_col_name = "Drug Molecule", "Target(s) Mentioned in Document"
        if drug_col_name not in header or target_col_name not in header: raise ValueError(f"CSV must contain '{drug_col_name}' and '{target_col_name}'")
        drug_col, target_col = header.index(drug_col_name), header.index(target_col_name)
        for row in reader:
            drug_name = row[drug_col].strip()
            matches = gene_regex.findall(row[target_col])
            if matches:
                for gene_list_str in matches:
                    true_targets_map[drug_name].extend([g.strip() for g in gene_list_str.split(',')])
    for drug in true_targets_map: true_targets_map[drug] = sorted(list(set(g for g in true_targets_map[drug] if g)))
    return true_targets_map

