def plot_inductive_performance_comparison(general_perf_file, personalized_perf_file):
    """
    Loads inductive test predictions and plots ROC and PR curves to compare
    the General and Personalized KG models.
    """
    try:
        general_df = pd.read_csv(general_perf_file)
        personalized_df = pd.read_csv(personalized_perf_file)
    except FileNotFoundError as e:
        print(f"FATAL ERROR: Could not find performance file: {e}. Skipping performance plot.")
        return

    fig, (ax1, ax2) = plt.subplots(1, 2, figsize=(16, 7))

    # --- ROC Curve ---
    for df, label, color in [(general_df, 'General KG', 'skyblue'), (personalized_df, 'Personalized KG', 'darkorange')]:
        fpr, tpr, _ = roc_curve(df['true_label'], df['prediction_score'])
        roc_auc = auc(fpr, tpr)
        ax1.plot(fpr, tpr, color=color, lw=3, label=f'{label} (AUC = {roc_auc:0.3f})')

    ax1.plot([0, 1], [0, 1], color='navy', lw=2, linestyle='--')
    ax1.set_xlim([0.0, 1.0])
    ax1.set_ylim([0.0, 1.05])
    ax1.set_xlabel('False Positive Rate', fontsize=12, weight='bold')
    ax1.set_ylabel('True Positive Rate', fontsize=12, weight='bold')
    ax1.set_title('Receiver Operating Characteristic (ROC)', fontsize=14, weight='bold')
    ax1.legend(loc="lower right", fontsize=11)
    ax1.grid(alpha=0.5)

    # --- Precision-Recall Curve ---
    for df, label, color in [(general_df, 'General KG', 'skyblue'), (personalized_df, 'Personalized KG', 'darkorange')]:
        precision, recall, _ = precision_recall_curve(df['true_label'], df['prediction_score'])
        ap = average_precision_score(df['true_label'], df['prediction_score'])
        ax2.plot(recall, precision, color=color, lw=3, label=f'{label} (AP = {ap:0.3f})')

    ax2.set_xlim([0.0, 1.0])
    ax2.set_ylim([0.0, 1.05])
    ax2.set_xlabel('Recall', fontsize=12, weight='bold')
    ax2.set_ylabel('Precision', fontsize=12, weight='bold')
    ax2.set_title('Precision-Recall Curve', fontsize=14, weight='bold')
    ax2.legend(loc="lower left", fontsize=11)
    ax2.grid(alpha=0.5)

    fig.suptitle('Inductive Test Performance Comparison', fontsize=18, weight='bold')
    plt.tight_layout(rect=[0, 0, 1, 0.95])

    filename = os.path.join(OUTPUT_DIR, "inductive_performance_comparison.png")
    plt.savefig(filename, dpi=300)
    plt.close()
    print(f"✅ Saved performance comparison plot: {filename}")


def visualize_hypothesis_subgraph(drug_name, predictions_df):
    """
    Visualizes the drug and its top predicted targets as a network graph.
    """
    subgraph = nx.Graph()
    subgraph.add_node(drug_name, type='drug')
    for _, row in predictions_df.iterrows():
        subgraph.add_node(row['gene'], type='gene')
        subgraph.add_edge(drug_name, row['gene'], weight=row['score'])

    # FIX: Create an explicit figure and axes object
    fig, ax = plt.subplots(figsize=(14, 10))
    pos = nx.spring_layout(subgraph, seed=SEED, k=0.8)

    node_sizes = [3000 if n == drug_name else s * 4000 + 500 for n, s in zip(subgraph.nodes(), [0] + list(predictions_df['score']))]

    logFCs = [0 if n == drug_name else predictions_df[predictions_df['gene'] == n]['logFC'].values[0] for n in subgraph.nodes()]
    cmap = sns.diverging_palette(240, 10, as_cmap=True)
    min_logfc, max_logfc = min(logFCs), max(logFCs)
    if min_logfc == max_logfc: min_logfc -= 0.1; max_logfc += 0.1
    norm = mcolors.TwoSlopeNorm(vmin=min_logfc, vcenter=0, vmax=max_logfc)
    node_colors_mapped = [cmap(norm(val)) for val in logFCs]

    nx.draw_networkx_nodes(subgraph, pos, ax=ax, node_size=node_sizes, node_color=node_colors_mapped, edgecolors='black', linewidths=0.5)
    nx.draw_networkx_edges(subgraph, pos, ax=ax, width=2, alpha=0.5, edge_color='gray')
    nx.draw_networkx_labels(subgraph, pos, ax=ax, font_size=10, font_weight='bold')

    sm = plt.cm.ScalarMappable(cmap=cmap, norm=norm); sm.set_array([])
    # FIX: Pass the axes object to the colorbar
    cbar = plt.colorbar(sm, ax=ax, shrink=0.7, aspect=15)
    cbar.set_label('logFC (Late vs. Early)', weight='bold')

    avg_logFC = predictions_df['logFC'].mean()
    if avg_logFC > 0.1: hypothesis = "Hypothesis: Effective in LATE-STAGE Fibrosis"
