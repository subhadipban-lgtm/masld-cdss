def plot_cross_drug_heatmap(full_predictions_df):
    """
    Generates a clustered heatmap of predicted target profiles from the main predictions dataframe.
    """
    heatmap_df = full_predictions_df.pivot_table(index='drug', columns='gene', values='score').fillna(0)

    if heatmap_df.empty:
        print("Could not create heatmap from the provided data.")
        return

    # Filter to genes predicted for at least two drugs for a cleaner plot
    heatmap_df = heatmap_df.loc[:, heatmap_df.astype(bool).sum(axis=0) >= 2]
    if heatmap_df.empty:
        print("No overlapping genes found among top predictions to create heatmap.")
        return

    sns.clustermap(heatmap_df, cmap="viridis", linewidths=.5, annot=False,
                   figsize=(max(8, 0.5 * len(heatmap_df.columns)), max(6, 0.4 * len(heatmap_df.index))),
                   cbar_kws={'label': 'Prediction Score'})

    plt.suptitle('Cross-Drug Predicted Target Similarity', fontsize=16, weight='bold', y=1.02)
    filename = os.path.join(OUTPUT_DIR, "cross_drug_target_heatmap.png")
    plt.savefig(filename, dpi=300, bbox_inches='tight')
    plt.close()
    print(f"✅ Saved cross-drug heatmap: {filename}")

def main():
    """ Main function to run the visualization script. """
    # 1. Generate Performance Comparison Plot
    print("\n--- Generating Inductive Performance Comparison Plot ---")
    plot_inductive_performance_comparison(GENERAL_KG_PERF_FILE, PERSONALIZED_KG_PERF_FILE)

    # 2. Load Main Predictions File
    print(f"\n--- Loading top target predictions from {PREDICTIONS_FILE} ---")
    try:
        # Rename columns on load to match what the functions expect
        predictions_df = pd.read_csv(PREDICTIONS_FILE).rename(columns={
            'predicted_target_gene': 'gene',
            'prediction_score': 'score',
            'dge_logFC_Late_vs_Early': 'logFC'
        })
        print(f"Found predictions for {len(predictions_df['drug'].unique())} unique drugs.")
    except FileNotFoundError:
        print(f"FATAL ERROR: Predictions file not found at '{PREDICTIONS_FILE}'. Cannot proceed.")
        return
    except KeyError:
        print(f"FATAL ERROR: The CSV {PREDICTIONS_FILE} does not contain the expected columns.")
        return

    # 3. Generate Visualizations for Each Drug
    unique_drugs = predictions_df['drug'].unique()
    for drug in unique_drugs:
        print(f"\n--- Generating visualizations for: {drug} ---")
        drug_specific_df = predictions_df[predictions_df['drug'] == drug]
        if drug_specific_df.empty:
            print(f"No data found for {drug}, skipping.")
            continue
        visualize_hypothesis_subgraph(drug, drug_specific_df)
        plot_prediction_dashboard(drug, drug_specific_df)

    # 4. Generate Cross-Drug Comparison Heatmap
    print("\n--- Generating Cross-Drug Target Similarity Heatmap ---")
    plot_cross_drug_heatmap(predictions_df)

    print("\n--- Visualization Script Finished ---")

if __name__ == "__main__":
    main()

