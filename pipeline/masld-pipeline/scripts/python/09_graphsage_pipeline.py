def preprocess_graph_for_pyg(G_nx, all_node_types, all_personalization_statuses):
    print(f"Preprocessing graph: {G_nx.number_of_nodes()} nodes, {G_nx.number_of_edges()} edges...")
    type_map = {name: i for i, name in enumerate(sorted(all_node_types))}
    status_map = {name: i for i, name in enumerate(sorted(all_personalization_statuses))}
    node_labels = sorted(G_nx.nodes())
    node_map_int = {label: i for i, label in enumerate(node_labels)}
    G_mapped_nx = nx.relabel_nodes(G_nx, node_map_int, copy=True)

    node_features = []
    for node_id in range(len(node_labels)):
        original_label = node_labels[node_id]
        data = G_nx.nodes[original_label]
        type_vec = torch.zeros(len(type_map))
        status_vec = torch.zeros(len(status_map))
        t = data.get("type", "unknown")
        s = data.get("personalization_status", "unknown")
        if t in type_map: type_vec[type_map[t]] = 1.0
        if s in status_map: status_vec[status_map[s]] = 1.0
        logFC = float(data.get("dge_logFC_Late_vs_Early", 0.0))
        pval = float(data.get("dge_adj_p_val", 1.0))
        is_suppressor = float(bool(data.get("is_suppressor", False)))
        in_fibrosis = float(bool(data.get("in_fibrosis_module", False)))
        is_driver = float(bool(data.get("is_driver", False)))
        pval_transformed = -np.log10(pval + 1e-12)
        pval_transformed = np.clip(pval_transformed, 0, 50)
        logFC = np.clip(logFC, -10, 10)
        numeric = torch.tensor([logFC, pval_transformed, is_suppressor, in_fibrosis, is_driver], dtype=torch.float)
        node_features.append(torch.cat([type_vec, status_vec, numeric]))

    x = torch.stack(node_features)
    edges = list(G_mapped_nx.edges())
    edge_index = torch.tensor(edges, dtype=torch.long).t().contiguous() if edges else torch.empty((2, 0), dtype=torch.long)
    data = Data(x=x, edge_index=to_undirected(edge_index, num_nodes=len(node_labels)), node_labels=node_labels, num_nodes=len(node_labels))
    return data, G_mapped_nx, node_map_int

