def run_inductive_experiment(gexf_file, title, all_node_types, all_personalization_statuses, holdout_drug_names, true_targets_map, save_dir):
    """
    MODIFIED:
    - Added `save_dir` parameter.
    - Saves model state dict to specified .pt file.
    - Saves training/validation curves.
    - Saves test performance curves.
    - Saves test predictions to CSV.
    """
    print(f"\n{'='*80}\nRUNNING INDUCTIVE PIPELINE FOR: {title}\n{'='*80}")
